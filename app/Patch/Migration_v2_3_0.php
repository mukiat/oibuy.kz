    /*
    * 商家可视化模板 模板色调选择
    */
    private function batchUpdateSellerTemplate($path = '')
    {
        if (empty($path)) {
            $path = Storage::path('data/seller_templates');
        }

        if (is_dir($path)) {
            $dir = scandir($path);

            foreach ($dir as $value) {
                $sub_path = $path . '/' . $value;

                if ($value == '.' || $value == '..') {
                    continue;
                } else if (is_dir($sub_path)) {
                    $this->batchUpdateSellerTemplate($sub_path);
                } else {
                    if (basename($value) == 'pc_page.php') {
                        $open_path = $path . '/' . $value;
                        $file_content = file_get_contents($open_path);
                        if ($file_content) {
                            if (strstr($file_content, 'default_temp') === false) {
                                $new_file_content = str_replace('class="demo ui-sortable"', 'class="demo ui-sortable" data-default="default_temp"', $file_content);
                                file_put_contents($open_path, $new_file_content);
                            }
                        }
                    }
                }
            }
        }
    }

